package neetCode150.binarySearch;

/**
 * <a href="https://leetcode.com/problems/search-in-rotated-sorted-array/">...</a>
 * There is an integer array nums sorted in ascending order (with distinct values).
 * Prior to being passed to your function, nums is possibly left rotated at an unknown index k (1 <= k < nums.length) such that the resulting array is [nums[k], nums[k+1], ..., nums[n-1], nums[0], nums[1], ..., nums[k-1]] (0-indexed). For example, [0,1,2,4,5,6,7] might be left rotated by 3 indices and become [4,5,6,7,0,1,2].
 * Given the array nums after the possible rotation and an integer target, return the index of target if it is in nums, or -1 if it is not in nums.
 * You must write an algorithm with O(log n) runtime complexity.
 *
 *  * Detailed notes and dry run:
 * <a href="=src/neetCode150/binarySearch/notes/SearchInRotatedSortedArray.MD">notes/SearchInRotatedSortedArray.MD</a>
 */

public class SearchInRotatedSortedArray {

    /**
     * Optimal solution:
     * We use modified binary search:
     * At every step, one half of the array(either nums[l] -> nums[mid] or nums[mid] -> nums[r]) is always sorted.
     * Check which half is sorted.
     * If the target lies inside the sorted half → shrink search to that half.
     * Otherwise → search the other half.
     * This keeps time complexity O(log n).
     *
     * Time Complexity : O(log(n))
     * Space Complexity : O(1)
     */
    public int search(int[] nums, int target) {
        int left = 0, right = nums.length - 1;

        while (left <= right) {
            int mid = left + (right - left) / 2;

            if (nums[mid] == target) return mid;

            // Left half is sorted
            if (nums[left] <= nums[mid]) {
                if (nums[left] <= target && target < nums[mid]) {
                    right = mid - 1;
                } else {
                    left = mid + 1;
                }
            }
            // Right half is sorted
            else {
                if (nums[mid] < target && target <= nums[right]) {
                    left = mid + 1;
                } else {
                    right = mid - 1;
                }
            }
        }

        return -1; // not found
    }
}

